import { DAY_KEYS, PopularityCurve, Spot } from "@/types";

// Builds a weekly busyness curve from a few human-readable knobs.
// We model each peak hour as a bell curve and sum them, so a spot can have
// e.g. a lunch peak and a dinner peak. Weekends are boosted. This is the
// "time-of-day model" — fully explainable, no black box.
function makeCurve(
  peakHours: number[],
  intensity: number, // 0..1 baseline popularity of this spot
  opts: { weekendBoost?: number; spread?: number } = {}
): PopularityCurve {
  const { weekendBoost = 1.35, spread = 2.4 } = opts;
  const curve = {} as PopularityCurve;

  for (const day of DAY_KEYS) {
    const isWeekend = day === "sat" || day === "sun";
    const hours: number[] = [];
    for (let h = 0; h < 24; h++) {
      let v = 0;
      for (const peak of peakHours) {
        v += Math.exp(-((h - peak) ** 2) / (2 * spread * spread));
      }
      v = v * intensity * 100;
      if (isWeekend) v *= weekendBoost;
      hours.push(Math.min(100, Math.round(v)));
    }
    curve[day] = hours;
  }
  return curve;
}

